package com.treeke.algorithm;

public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, com.treeke.algorithm.ListNode next) {
        this.val = val;
        this.next = next;
    }
}
